package entity;

public class TwoColumns {
	private Integer columnsId;
	private Integer relationId;
	private Integer columnPosition;
	private Integer columnClass;
	private String columnName;
	public Integer getColumnsId() {
		return columnsId;
	}
	public void setColumnsId(Integer columnsId) {
		this.columnsId = columnsId;
	}
	public Integer getRelationId() {
		return relationId;
	}
	public void setRelationId(Integer relationId) {
		this.relationId = relationId;
	}
	public Integer getColumnPosition() {
		return columnPosition;
	}
	public void setColumnPosition(Integer columnPosition) {
		this.columnPosition = columnPosition;
	}
	public Integer getColumnClass() {
		return columnClass;
	}
	public void setColumnClass(Integer columnClass) {
		this.columnClass = columnClass;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	
}
