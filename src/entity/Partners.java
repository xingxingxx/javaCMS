package entity;

public class Partners {
	private Integer partnersId;
	private String description;
	private String imageUrl;
	private String url;
	private Integer dispalyNum;
	public Integer getPartnersId() {
		return partnersId;
	}
	public void setPartnersId(Integer partnersId) {
		this.partnersId = partnersId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getDispalyNum() {
		return dispalyNum;
	}
	public void setDispalyNum(Integer dispalyNum) {
		this.dispalyNum = dispalyNum;
	}
	
}
